def pug_is_dog(dog1, dog2, dog3):
    print(f"My {dog1} is good dog.")
    print(f"My {dog2} is gooder dog.")
    print(f"My {dog3} is the goodest dog.\n")

pug_is_dog(input(), input(), input())

pug_is_dog("Poodle", "Corgi", "Pug")

derp1 = "derp"
derp2 = "herp"
derp3 = "wololo"
pug_is_dog(derp1, derp2, derp3)
